#!/usr/bin/env python3
"""
A type-annotated function 'concat' that takes
a string str1 and a string str2 as arguments

Arguments:
    str1: a string
    str2: a string

Returns:
    a concatenated string
"""


def concat(str1: str, str2: str) -> str:
    """
    Returns a concatenated string of str1 and str2
    """
    return str1 + str2
